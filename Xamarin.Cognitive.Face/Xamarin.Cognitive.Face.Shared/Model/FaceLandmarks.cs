namespace Xamarin.Cognitive.Face.Model
{
	/// <summary>
	/// Defines a list of Face landmarks for a Face.
	/// </summary>
	public class FaceLandmarks
	{
		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the pupil left face landmark.
		/// </summary>
		public FeatureCoordinate PupilLeft { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the pupil right face landmark.
		/// </summary>
		public FeatureCoordinate PupilRight { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the nose tip face landmark.
		/// </summary>
		public FeatureCoordinate NoseTip { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the mouth left face landmark.
		/// </summary>
		public FeatureCoordinate MouthLeft { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the mouth right face landmark.
		/// </summary>
		public FeatureCoordinate MouthRight { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eyebrow left outer face landmark.
		/// </summary>
		public FeatureCoordinate EyebrowLeftOuter { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eyebrow left inner face landmark.
		/// </summary>
		public FeatureCoordinate EyebrowLeftInner { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eye left outer face landmark.
		/// </summary>
		public FeatureCoordinate EyeLeftOuter { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eye left top face landmark.
		/// </summary>
		public FeatureCoordinate EyeLeftTop { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eye left bottom face landmark.
		/// </summary>
		public FeatureCoordinate EyeLeftBottom { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eye left inner face landmark.
		/// </summary>
		public FeatureCoordinate EyeLeftInner { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eyebrow right inner face landmark.
		/// </summary>
		public FeatureCoordinate EyebrowRightInner { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eyebrow right outer face landmark.
		/// </summary>
		public FeatureCoordinate EyebrowRightOuter { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eye right inner face landmark.
		/// </summary>
		public FeatureCoordinate EyeRightInner { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eye right top face landmark.
		/// </summary>
		public FeatureCoordinate EyeRightTop { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eye right bottom face landmark.
		/// </summary>
		public FeatureCoordinate EyeRightBottom { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the eye right outer face landmark.
		/// </summary>
		public FeatureCoordinate EyeRightOuter { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the nose root left face landmark.
		/// </summary>
		public FeatureCoordinate NoseRootLeft { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the nose root right face landmark.
		/// </summary>
		public FeatureCoordinate NoseRootRight { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the nose left alar top face landmark.
		/// </summary>
		public FeatureCoordinate NoseLeftAlarTop { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the nose right alar top face landmark.
		/// </summary>
		public FeatureCoordinate NoseRightAlarTop { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the nose left alar out tip face landmark.
		/// </summary>
		public FeatureCoordinate NoseLeftAlarOutTip { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the nose right alar out tip face landmark.
		/// </summary>
		public FeatureCoordinate NoseRightAlarOutTip { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the upper lip top face landmark.
		/// </summary>
		public FeatureCoordinate UpperLipTop { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the upper lip bottom face landmark.
		/// </summary>
		public FeatureCoordinate UpperLipBottom { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the under lip top face landmark.
		/// </summary>
		public FeatureCoordinate UnderLipTop { get; set; }


		/// <summary>
		/// Gets or sets a <see cref="FeatureCoordinate"/> defining the under lip bottom face landmark.
		/// </summary>
		public FeatureCoordinate UnderLipBottom { get; set; }
	}
}