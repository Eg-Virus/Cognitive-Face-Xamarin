namespace Xamarin.Cognitive.Face.Model
{
	/// <summary>
	/// Face attribute type.
	/// </summary>
	public enum FaceAttributeType
	{
		/// <summary>
		/// The approximate age of the face.
		/// </summary>
		Age,

		/// <summary>
		/// The gender of the face.
		/// </summary>
		Gender,

		/// <summary>
		/// Smile intensity.
		/// </summary>
		Smile,

		/// <summary>
		/// Glasses type of the face.
		/// </summary>
		Glasses,

		/// <summary>
		/// Facial hair found on the face.
		/// </summary>
		FacialHair,

		/// <summary>
		/// The head pose of the head/face.
		/// </summary>
		HeadPose,

		/// <summary>
		/// The emotion detected on the face.
		/// </summary>
		Emotion,

		/// <summary>
		/// The hair of the head/face.
		/// </summary>
		Hair,

		/// <summary>
		/// The makeup found on the face.
		/// </summary>
		Makeup,

		/// <summary>
		/// Represents the occlusion of the face.
		/// </summary>
		Occlusion,

		/// <summary>
		/// The accessories found on the face.
		/// </summary>
		Accessories,

		/// <summary>
		/// The blur value of the face.
		/// </summary>
		Blur,

		/// <summary>
		/// The exposure value of the face.
		/// </summary>
		Exposure,

		/// <summary>
		/// The noise value of the face.
		/// </summary>
		Noise
	}
}